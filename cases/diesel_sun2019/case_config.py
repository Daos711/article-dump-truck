"""Параметры дизельного main bearing — Sun-geometry surrogate.

Геометрия коренной опоры взята из Sun et al. (2019), Chinese Journal
of Mechanical Engineering 32:23. Нагрузка построена surrogate-методом
(indicator-driven Gaussian gas pulse + inertia), не из оригинальных
данных статьи, поэтому кейс называется "Sun-geometry surrogate".
"""

# ─── Геометрия ─────────────────────────────────────────────────────
R = 0.040              # радиус journal, м (диаметр 80 мм)
L_bearing = 0.027      # ширина подшипника, м
c = 0.000146           # радиальный зазор, м (146 мкм)

# ─── Масло ─────────────────────────────────────────────────────────
eta = 0.014347         # динамическая вязкость, Па·с

# ─── Режим ─────────────────────────────────────────────────────────
n_rpm = 2200           # обороты (начальный кейс)
load_pct = 20          # процент нагрузки (мягкий старт)

# ─── Двигатель (Sun et al. 2019, Table 1) ──────────────────────────
n_cylinders = 4
n_strokes = 4          # 4-тактный → 720° CA = один цикл

# Surrogate ICE parameters (для build_load_from_indicator.py)
bore_m = 0.105         # диаметр цилиндра, м (Sun 2019)
stroke_m = 0.118       # ход поршня, м (Sun 2019)
con_rod_m = 0.210      # длина шатуна, м
m_piston_kg = 0.8      # масса поршневой группы, кг
p_max_MPa = 8.0        # пиковое давление газа при 100% load
p_motoring_MPa = 2.0   # давление на такте сжатия

# ─── Сетка ─────────────────────────────────────────────────────────
N1 = 200               # физические ячейки по φ
N2 = 20                # физические ячейки по Z

# ─── Начальные условия ─────────────────────────────────────────────
X0 = 0.0
Y0 = 0.8               # ближе к равновесию при высокой нагрузке

# ─── Параметры расчёта ─────────────────────────────────────────────
# Шаг по углу поворота коленвала, не по абстрактному времени.
# NT_per_cycle = 720° / crank_step_deg. dt = CYCLE_TAU / NT_per_cycle.
crank_step_deg = 0.5   # 0.5° CA на шаг → NT = 1440 на цикл
dt = None              # вычисляется в скриптах из crank_step_deg
max_cycles = 8         # максимум циклов (до сходимости)
warmup_cycles = 3      # минимум циклов до оценки convergence

# ─── Solver ────────────────────────────────────────────────────────
omega_p = 1.0
omega_theta = 1.0
tol_inner = 1e-6
max_inner = 5000
scheme = "rb"

# ─── Критические пороги (postprocessing) ───────────────────────────
h_crit_um = [5, 10]    # мкм
p_crit_MPa = [50, 100] # МПа
